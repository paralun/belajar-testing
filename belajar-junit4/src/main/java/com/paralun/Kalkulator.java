package com.paralun;

public class Kalkulator {

    public int addInt(int bil1, int bil2) {
        return bil1 + bil2;
    }

    public int subtractInt(int bil1, int bil2) {
        return bil1 - bil2;
    }

    public int multiply(int bil1, int bil2) {
        return bil1 * bil2;
    }

    public float div(float bil1, float bil2) {
        return bil1 / bil2;
    }
}
